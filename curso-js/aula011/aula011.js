//Aula sobre typeof 
let v1 = 10.4;
let v2 = "10";
let v3 = v1 === v2;
let v4 = {nome: "Ghislane"};

//O operador typeof retorna o nome do tipo do valor

console.log("Valor: " + v1 + " Tipo: " + typeof(v1)); //Tipo number
console.log("Valor: " + v2 + " Tipo: " + typeof(v2)); //Tipo string
console.log("Valor: " + v3 + " Tipo: " + typeof(v3)); //Tipo boolean
console.log("Valor: " + v4 + " Tipo: " + typeof(v4)); //Tipo objeto, uma array
